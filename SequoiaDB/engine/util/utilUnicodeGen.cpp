const static UINT32 utf8TableSize = sizeof(utf8Table)/sizeof(UINT32) ;

INT32 genChar ( CHAR *pBuffer, UINT32 &len )
{
   INT32 rc = SDB_OK ;
   UINT32 c = utf8Table[ ossRand() % utf8TableSize ] ;
   CHAR b = 0 ;
   INT32 s = 0 ;
   if ( !len )
      goto done ;
  if ( len <= 4 )
   {
      pBuffer[0] = 0 ;
      len = 0 ;
      goto done ;
   }
   b = (c&0xFF000000)>>24 ;
   if ( b )
   {
      pBuffer[s] = b ;
      s++ ;
   }
   b = (c&0x00FF0000)>>16 ;
   if ( b )
   {
      pBuffer[s] = b ;
      s++ ;
   }
   b = (c&0x0000FF00)>>8 ;
   if ( b )
   {
      pBuffer[s] = b ;
      s++ ;
   }
   b = (c&0x000000FF) ;
   if ( b )
   {
      pBuffer[s] = b ;
      s++ ;
   }
   len -= s ;
done :
   return rc ;
}

INT32 utilUnicodeGen ( CHAR *pBuffer, UINT32 len )
{
   INT32 rc = SDB_OK ;
   UINT32 tLen = len ;
   if ( 0 == len )
   {
      return SDB_INVALIDARG ;
      goto error ;
   }
   while ( SDB_OK == rc && len > 0 )
   {
      rc = genChar ( &pBuffer[tLen-len], len ) ;
   }
done :
   return rc ;
error :
   goto done ;
}
